
// # 12. Todo lo que debes saber de las variables let

// La única diferiencia entre var y let es lo que se le conoce como el scope
// de las variables, esto se verá más adelante.

// let te permite crear una variable y darle un valor.
let producto = 'Tablet';

// También te permite reasignarle un valor.

producto = "Monitor";

producto = 20;

producto = true;

producto = null;

console.log(producto);

// Podemos también crear la variable sin darle ningún valor.
let precio;

console.log(precio);