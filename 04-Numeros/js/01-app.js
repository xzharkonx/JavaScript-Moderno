
// # 21. Crear Números en JavaScript

// Creación de números
const numero1 = 20;
const numero2 = "20";

// Al ser de tipo número se pinta de color azul en la consola.
console.log(numero1);
// Al ser de tipo String se pinta de color negro en la consola.
console.log(numero2);

const num1 = 30;
const num2 = 20;
const num3 = 20.20;
const num4 = .102030;
const num5 = -20;

console.log(num1);
console.log(num2);
console.log(num3);
console.log(num4);
console.log(num5);

// * Otra forma de crear números. Aunque no es muy común.

const numero6 = new Number(80);
console.log(numero6);

