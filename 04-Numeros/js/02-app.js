
// # 22. Operaciones en JavaScript

const numero1 = 30;
const numero2 = 20;    

let resultado;

// ## suma

resultado = numero1 + numero2;
console.log(resultado);

// ## Resta

resultado = numero1 - numero2;
console.log(resultado);


// ## Multiplicación

resultado = numero1 * numero2;
console.log(resultado);


// ## División 

resultado = numero1 / numero2;
console.log(resultado);


// ## Modúlo

resultado = numero1 % numero2;
console.log(resultado);