
// # 25. Incremento o Decremento

let puntaje = 5;
console.log(puntaje);

// ## Incremento
// puntaje++; // 5
// ++puntaje; // 6

// ## Decremento
// puntaje--; // 5
// --puntaje; // 4

// console.log(puntaje++); // * La primera vez no incrementa.
// console.log(puntaje++); // * La segunda vez si incrementa.

// * Así incrementará inmediatamente
console.log(++puntaje);

// ## Otra forma de sumar pero más valores

// Incremento: +=
// Decremento: -=

puntaje += 3;
// puntaje -= 3;
console.log(puntaje);